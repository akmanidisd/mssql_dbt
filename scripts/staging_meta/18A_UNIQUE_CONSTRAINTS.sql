SELECT
    s.name        AS schema_name,
    t.name        AS table_name,
    i.name        AS index_name,
    i.is_unique,
    i.is_primary_key,
    i.type_desc
FROM sys.indexes i
JOIN sys.tables t
  ON t.object_id = i.object_id
JOIN sys.schemas s
  ON s.schema_id = t.schema_id
WHERE 1 = 1
  AND s.name = 'dbo'
  AND SUBSTRING(t.name,1,1) != '_'
  AND t.name NOT LIKE 'zKill%'
  AND t.name NOT LIKE 'sys%'
  ORDER BY 1,2,3
;

